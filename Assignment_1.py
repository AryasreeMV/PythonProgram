# Program to perform basic arithmetic operations and string concatenation

# Fetching two user inputs and storing them as integers
num1 = int(input("Enter first number : ")) 
num2 = int(input("Enter second number : "))

# Performing arithmetic operations and printing the results
print("Sum is: ",num1+num2) 
print("Difference is: ", num1-num2)
print("Product is: ", num1*num2)
print("Quotient is: ", num1/num2)


# String concatenation after type conversion to string
print("Concatenated string is: ", str(num1) + str(num2))